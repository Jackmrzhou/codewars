def boolfuck(code, input=""):
	mem = []
	output = []
	code = list(code)
	input_s = []
	for c in input:
		b = bin(ord(c))[2:]
		if len(b) < 8:
			b = '0'*(8-len(b))+b
		input_s.append(b[::-1])
	input_s = list(''.join(input_s))
	left =[]
	loop_map = {}
	for i,it in enumerate(code):
		if it == '[':
			left.append(i)
		if it == ']':
			l = left.pop()
			loop_map[l] = i
			loop_map[i] = l
	pointer = 0
	pc = 0
	while pc < len(code):
		if pointer>= len(mem):
			mem.append(0)
		if code[pc] == '>':
			pointer += 1
		elif code[pc] == '<':
			pointer -= 1
			if pointer < 0:
				pointer = 0
				mem.insert(0,0)
		elif code[pc] == '+':
			mem[pointer] = 1 - mem[pointer]
		elif code[pc] == ',':
			try:
				mem[pointer] = int(input_s.pop(0))
			except:
				mem[pointer] = 0
		elif code[pc] == ';':
			output.append(str(mem[pointer]))
		elif code[pc] == '[':
			if mem[pointer]==0:
				pc = loop_map[pc]
		elif code[pc] == ']':
			pc = loop_map[pc]-1
		pc += 1
	if output:
		for _ in range(8-len(output) % 8 if len(output) % 8 else 0):
			output.append('0')
	res = ''
	for i in range(0,len(output),8):
		res += chr(int(''.join(output.pop() for _ in range(8)), base = 2))
	return res[::-1]

